package htb.blockgame;

public class BlockInformation {
    private int X;
    private int Y;
    private int Z;

    public int X() {
        return X;
    }

    public int Y() {
        return Y;
    }

    public int Z() {
        return Z;
    }

    public void setX(int modX) {
        X = modX;
    }

    public void setY(int modY) {
        Y = modY;
    }

    public void setZ(int modZ) {
        Z = modZ;
    }
}
